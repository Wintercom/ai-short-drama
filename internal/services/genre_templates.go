// 本文件是离线 Stub 的"题材模板库"：为不同题材预设差异化的
// 主题、节拍、角色与分镜，让零成本离线模式下 genre 也能真正影响成片。

// stubChar 是模板中的角色设定。
type stubChar struct {
	name       string
	persona    string
	appearance string
	gender     string // male/female，决定配音男/女声
}

// stubShot 是模板中的单个镜头。
type stubShot struct {
	heading   string
	location  string
	timeOfDay string
	charID    string
	shotType  string
	camera    string
	keyframe  string
	dialogue  string
}

// genreTemplate 是一套完整的题材模板。
type genreTemplate struct {
	tone     string // 基调形容词，嵌入 logline
	theme    string // 主题
	synopsis string // 梗概后半句
	beats    []string
	lead     stubChar
	support  stubChar
	shots    []stubShot
}

// pickTemplate 按题材选取模板，支持模糊包含匹配（如"治愈系"→"治愈"）；未命中回退默认。
func pickTemplate(genre string) genreTemplate {
	g := strings.TrimSpace(genre)
	for _, key := range genreKeys {
		if strings.Contains(g, key) {
			return genreTemplates[key]
		}
	}
	return genreTemplates["都市"]
}

